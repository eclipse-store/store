public enum EnumerationCopy {

    FIRST("first", "10"),
    SECOND("second", "30")

    ;

    private String name;
    private String value;

    EnumerationCopy(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public String getValue() {
        return value;
    }
}
